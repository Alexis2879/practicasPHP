<?php
  /* Operadores aritmeticos
     + , - , / , * 
  */
  $a = 7;
  $b = 11.5;
  echo "Valor A: ".$a;
  echo "Valor B: ".$b;
  //suma
  $resultado = $a + $b;
  echo "La suma es: ".$resultado;

  // resta
  $resultado = $a - $b;
  echo "La resta es: ".$resultado;

  //Division
  $resultado = $a / $b;
  echo "La division es: ".$resultado;

  //multiplicacion
  $resultado = $a * $b;
  echo "La Multiplicacion es: ".$resultado;
?>